$(document).ready(function() {

    // Traduzir mensagem de required (HTML5)
    var elements = document.getElementsByTagName("INPUT");
    for (var i = 0; i < elements.length; i++) {
        elements[i].oninvalid = function(e) {
            e.target.setCustomValidity("");
            if (!e.target.validity.valid) {
                e.target.setCustomValidity("Campo obrigatório.");
            }
        };
        elements[i].oninput = function(e) {
            e.target.setCustomValidity("");
        };
    }


    $('input[type=radio][name=categoria_id]').change(function() {
        if (this.value == '2') {
            $('.palestrante-itens').show();
        } else {
          $('.palestrante-itens').hide();
        }
    });


    $("#submit-inscrever").on("click", function(e){
        e.preventDefault();
        
        var email = $("#email-inscrever").val();
        var emailCadastro = $("#email-cadastro").val(email.toLowerCase());
    });

 

    $('[name=telefone]').mask('(00) 00000-0000');

    $( "#form-inscrito" ).submit(function(e) {
      e.preventDefault();
     
      var values = $(this).serialize();

      $.ajax({
          url: "flisol2016/controller/FlisolController.php",
          type: "post",
          data: values,
          success: function (response) {  
              $('#myModal-msg').html(response);
              $('#myModal').modal();
              $('#form-inscrito')[0].reset();   
              grecaptcha.reset();
          },
          error: function(jqXHR, textStatus, errorThrown) {
              console.log(textStatus, errorThrown);
          }
      });

    });
});



